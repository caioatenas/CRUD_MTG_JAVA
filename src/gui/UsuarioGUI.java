    private void tbnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnSalvarActionPerformed

        objUsuario = new Usuario();
        objUsuario.setId(txtID.getText());
        objUsuario.setCarta(txtCarta.getText());
        objUsuario.setEdi(txtEdi.getText());
        objUsuario.setIdi(txtIdi.getText());
        objUsuario.setFoil(txtFoil.getText());
        objUsuario.setValor(txtValor.getText());
        objUsuario.setQud(txtQud.getText());

        // fazendo a validação dos dados
        if ((txtCarta.getText().isEmpty()) || (txtEdi.getText().isEmpty()) || (txtIdi.getText().isEmpty()) || (txtFoil.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Informe valores para os campos");
        } else {
            // instanciando a classe UsuarioDAO do pacote dao e criando seu objeto dao
            objDAO = new UsuarioDAO();
            objDAO.salvar(objUsuario);
            JOptionPane.showMessageDialog(null, "Usuário " + txtCarta.getText() + " inserido com sucesso! ");
        }

        carregarTable(null);

        // apaga os dados preenchidos nos campos de texto
        setClear();
    }//GEN-LAST:event_tbnSalvarActionPerformed

